	    <ul class="list-group fl-color mx-0">
	      <li class="list-group-item">
	        <h3 class="fl-txt-rose">UBBC Solo</h3>
	        <p class="fl-txt-lesslight">C’est l’UBBC en mode cool. Sans chrono, sans pression, sans classement à tout prix. Un tour suffit. Deux, si l’envie revient. Le principe est simple: chacun fait autant de boucles qu’il veut, ou qu’il peut. Et surtout, à son rythme. On peut s’arrêter au ravito, papoter, faire une pause yoga, s’allonger dans l’herbe ou regarder les autres souffrir.
                À chaque passage, on enregistre son tour sur la table de marque, comme dans un jeu un peu absurde mais étonnamment satisfaisant. L’objectif officiel (si on tient à ce mot): cumuler le plus de tours entre le départ et la fin de l’épreuve. Mais il n’est pas obligatoire de rester toute la nuit pour participer.
                Celles et ceux qui auront couvert la plus grande distance seront déclarés vainqueurs. Mais entre nous, le vrai titre, c’est d’avoir passé une soirée entière à courir dans un parc avec des amis et trouvé ça parfaitement normal. </p>
	      </li>
	      <li id="funky" class="list-group-item">
             <h3 class="fl-txt-rose mb-0 mr-2">UBBC-Puebla</h3>
	        <p class="fl-txt-lesslight">En achevant deux tours du parcours <strong>UBBC 10ème anniversaire</strong> (soit 9,5 km et 460 m de D+), vous devenez officiellement finisher de l’UBBC-Puebla. C’est la plus accessible de nos quatre épreuves. Enfin… accessible à condition d’avoir envie de grimper l’équivalent de deux fois la tour Eiffel, en baskets, en pleine ville.
                Parce que oui, ici, on court à Paris mais avec les jambes en montagne.
                Et après ? Vous pourrez profiter sans limite de l’ambiance sur notre base camp, refaire la course avec des inconnu·e·s devenu·e·s copain·e·s, et papoter sous les guirlandes jusqu’à ce que les jambes cessent de trembler.
            </p>
