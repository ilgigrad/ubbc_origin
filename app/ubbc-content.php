	        <p class="fl-txt-lesslight">En achevant huit tours du parcours <strong>UBBC 10ème anniversaire</strong> (38 km et 1850 de D+), vous devenez finisher de l’UBBC-Hipster. C’est le format le plus populaire de l’événement. Un défi exigeant mais fréquentable, où l’on brûle des mollets tout en gardant le sourire au ravito.
                Compétitif, oui. Convivial, toujours. Vous y laisserez un peu de quadriceps, beaucoup de sueur, et peut-être votre dignité dans la dernière montée, mais vous repartirez avec une banane et des copains.
            </p>
	      </li>
				<li  id="k2" class="list-group-item">
