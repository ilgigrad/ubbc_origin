		<div class="row no-gutters no-wrap">
			<div class="col-12 col-md-4 px-3 mx-0">
		    <h3 class="fl-txt-peach text-center"><i class="fal fa-rabbit-fast pr-2"></i>Off 2 Ouf</h3>
		    <p class="fl-txt-md fl-txt-dark text-justify">l'UBBC n'est pas une course officielle. Initié sur le forum <a class="fl-txt-blood" href="http://www.kikourou.net/forum/viewtopic.php?f=19&amp;t=36996">kikourou</a>, cet événement n'est pas tout à fait compétitif. C'est d'abord un rassemblement d'amis et de passionnés de la course à pied et du trail, y compris en milieu urbain. Pas de dossard, pas de frais d'inscription, pas de certificat médical ou de licence. Vous êtes libres de venir et de courir avec nous sur le parcours sans plus de formalités que ça. Nous ne vous demandons de vous soumettre à aucun règlement autre que celui du parc. Un OFF donc. Mais un Off de fou parce que nous serons nombreux et que nous voulons faire de cet événement, un moment convivial et heureux; Une fête de la course dans un environnement magique et juqu'au bout de la nuit</p>
		  </div>
			<div class="col-12 col-md-4 px-3 mx-0">
		  	<h3 class="fl-txt-peach text-center" ><i class="fal fa-head-vr pr-2"></i>Buttes Chaumont augmentées</h3>
		  	<p class="fl-txt-md fl-txt-dark text-justify">l'UBBC  est d'abord une jolie balade à travers le parc le plus romantique le plus pittoresque et le plus escarpé de Paris. Un parcours vallonné et paysager qui emprunte tous les passages emblématiques du parc et  vous fera découvrir de nouveaux horizons à chaque tour: la passerelle  suspendue de Gustave Eiffel, l'ile du Belvédère, le pont des suicidés, le kiosque à musique, la cascade, la grotte, la rivière, le lac, la petite ceinture, le Pavillon Puebla et le Rosa Bonheur. Avec des dizaines d'athlètes dans le classement <a class="fl-txt-blood" href="https://www.strava.com/segments/12709171" target="_blank" rel="noopener noreferrer">Strava</a>, l'UBBC  est devenue un passage obligé de tous ceux qui courent <em>vraiment</em> aux Buttes Chaumont. Les côtes que vous découvrirez dans les Buttes Chaumont seront votre chemin de croix; celui de vos muscles, de votre coeur et de vos poumons. l'UBBC  vous en propose le meilleur.</p>
			</div>
			<div class="col-12 col-md-4 px-3 mx-0">
				<h3 class="fl-txt-peach text-center"><i class="fal fa-mountain fl-txt-40 pr-2"></i>la boucle ultime</h3>
		  	<p class="fl-txt-md fl-txt-dark text-justify">En conservant l'esprit des boucles UBBC Origin™, et UBBC 2.0™ et 3.02™,<strong> le parcours UBBC IV™</strong> vous offrira l'essence des Buttes Chaumont, sa beauté et ses bosses. Avec moins de 5km et près de 170m d+, cette nouvelle trace vous fera explorer tous les chemins les points hauts et les points bas du parc. Une première partie plus rapide qui vous permettra de profiter d'un panorama sublime qui s'étend jusqu'à la colline de Montmartre; une seconde partie plus sportive dans laquelle vous devrez enchaîner des montées et des descentes épuisantes. Distance et dénivelé sont parfaitement optimisés et nous pouvons fièrement prétendre à avoir enfin dessiné la boucle ultime.</p>
			</div>
