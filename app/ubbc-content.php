		<h1 class="text-center fl-txt-prune">ESPRIT UBBC</h1>
		<div class="row no-gutters no-wrap">
			<div class="col-12 col-md-4 px-3 mx-0">
		    <h3 class="fl-txt-peach text-center"><i class="fal fa-sun pr-2"></i>Off de oufs</h3>
		    <p class="fl-txt-md fl-txt-dark text-justify">L’UBBC n’est pas une course officielle. Pas de dossard, pas de frais d’inscription, pas de certificat médical. Personne pour vérifier si ta montre est chargée ou si tu transportes une veste GoreTex dans ton sac. C’est un rassemblement libre de coureurs un peu obsessionnels, amateurs de sentiers urbains et de boucles répétées, venus partager un effort, une bière, un bout de nuit. Pas de règlement, sinon celui du parc. Un *off*, donc. Mais un *off* lumineux : parce qu’on sera nombreux, parce que les frontales dessinent des halos étranges dans les allées sombres, et parce que courir en rond, ensemble, jusqu’au bout de la nuit, ça a quelque chose de magique et de joyeusement inutile.  </p>
		  </div>
			<div class="col-12 col-md-4 px-3 mx-0">
		  	<h3 class="fl-txt-peach text-center" ><i class="fal fa-cable-car pr-2"></i>Buttes Chaumont augmentées</h3>
		  	<p class="fl-txt-md fl-txt-dark text-justify">L’UBBC, c’est avant tout une jolie balade, enfin une balade avec du dénivelé, des relances, des escaliers, et quelques pensées sombres à chaque tour. Le parc le plus romantique, le plus escarpé, et probablement le plus casse-pattes de Paris vous déroule ses allées: la passerelle suspendue signée Gustave Eiffel, le belvédère, le pont des suicidés (nommément), la cascade, la grotte, la rivière, le lac, la petite ceinture… sans oublier les lieux de perdition post-effort: le Pavillon Puebla et le Rosa Bonheur. Chaque tour redessine le paysage, chaque montée rappelle qu’on n’est pas venus là pour flâner.
                L’UBBC est devenue, sans vraiment le vouloir, un classique. Des dizaines d’athlètes y laissent des segments Strava comme on grave ses initiales dans l’écorce d’un arbre. Ceux qui courent <em>vraiment</em> aux Buttes y passent un jour ou l’autre. Et ceux qui y reviennent savent que les côtes du parc ne font jamais de cadeau: elles t’attrapent aux jambes, te pincent les poumons, et s’installent dans ton cœur comme un souvenir un peu douloureux, mais auquel, bizarrement, tu tiens.  </p>
			</div>
			<div class="col-12 col-md-4 px-3 mx-0">
				<h3 class="fl-txt-peach text-center"><i class="fal fa-mountain fl-txt-40 pr-2"></i>la boucle ultime</h3>
		  	<p class="fl-txt-md fl-txt-dark text-justify">Dans la lignée des boucles UBBC Origin™, UBBC 2.0™, 3.02™, IV™, reboot.6, UBBC-7, UBBC-8, UBBC-9, le parcours de l’<strong>UBBC 10ème anniversaire</strong> concentre l’essence des Buttes Chaumont : leur beauté, leurs bosses, et un goût prononcé pour la répétition. Sur 4,7km et 230 de D+, cette nouvelle trace s’adapte aux contraintes du parc, sans rien céder à l’esprit. La passerelle et le belvédère sont inaccessibles ? Très bien. On sort des chemins, on traverse les pentes, on cherche les recoins oubliés.
                Dès le départ, le ton est donné. Descente vers la rivière, puis remontée sèche dans l’herbe jusqu’au pont sous le belvédère. Ensuite, une série de courtes montées et descentes bien cassantes sur l’est du parc. Au troisième kilomètre, un passage au-dessus de la petite ceinture. Et pour finir, une dernière section toujours aussi raide, toujours aussi sauvage. Pas de répit, pas d’issue.
                Distance et dénivelé sont désormais parfaitement optimisés. On peut le dire sans rougir : notre algorithme a enfin tracé la boucle parfaite.
            </p>
			</div>
