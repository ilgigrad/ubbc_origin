	        <p class="fl-txt-lesslight">En achevant cinq tours du parcours <strong>UBBC 10ème anniversaire</strong> (23,5 km et 1150 m de D+), vous devenez finisher de l’UBBC-Funky. Là, on commence à parler sérieusement. Avec un ratio dénivelé/distance difficile à battre en Île-de-France, l’effort demandé n’est pas loin de celui d’un marathon. Mais en plus court, en plus raide, et probablement en plus absurde.
                C’est la meilleure façon de découvrir ce que les Buttes Chaumont ont de plus exigeant à offrir: des montées, des descentes, des relances, et des zones de pique-nique en embuscade.
            </p>
