        <h3>OFF-ETIQUETTE</h3>
        <ul>
          <li><i>XX ad libitum </i>est une course OFF</li>
          <li>Ce n’est pas une épreuve compétitive en ce sens que nous souhaitons mettre en avant l’esprit de partage et le plaisir de courir ensemble</li>
          <li>l’objet de <i>XX ad libitum </i>est de courir autant que l’on peut tout en visitant dans ses moindres recoins, un des arrondissements les plus emblématiques de Paris; celui qui mêle délicieusement no-go-zones et bobo-zones.</li>
          <li>Elle est très librement organisée et ne dépend d’aucune association, enseigne, marque et entreprise</li>
        </ul>
      </div>
      <div>
        <ul>
          <li>Il n’y aura pas de classement officiel.</li>
          <li>L’épreuve ne permettra d’acquérir aucun point ITRA et ne sera pas prise en compte dans le calcul d’une cote de performance quelconque</li>
          <li>Les participants courent sous leur propre responsabilité et devront, entre autre, respecter les règles de circulation en milieu urbain (utilisation des trottoirs, respects des feux tricolores et des passages piétons)</li>
          <li>La course est limitée à 50 participants</li>
          <li>A l’instar de l’UBBC, le ravitaillement est collectif et autogéré: Chacun contribue au buffet en apportant boisson et nourriture.</li>
          <li>Le vestiaire est gardé collectivement par les participants qui ne courent pas</li>
        </ul>
      </div>
      <div>
        <h1>PARCOURS</h1>
        <ul>
          <li>XX ad libitum est composé de 4 boucles de 8 km et de 100m d+ environ chacune.</li>
          <li>Chaque boucle sera parcouru une première fois dans le sens dit « normal » puis, lorsque toutes les boucles auront été parcourues, elles seront reprises en sens inverse.</li>
          <li>Le parcours en sens normal couvre une distance de 32km; le parcours normal et inversé couvre donc une distance totale de 64km.</li>
          <li>Les boucles couvrent des zones différentes du vingtième arrondissement et une partie du XIXème arrondissement.</li>
          <li>Le parcours permet notamment d’explorer les quartiers de Belleville, Gambetta, Ménilmontant, la Mouzaïa et la campagne à Paris.</li>
          <li>Les traces sont sinueuses et se chevauchent parfois; chaque boucle est donc matérialisée par un balisage de couleur différente.</li>
          <li>Il convient néanmoins de bien rester concentré sur le balisage de la boucle que l’on emprunte et de ne pas se diriger en direction d’un coureur que l’on aperçoit devant soi.</li>
        </ul>
