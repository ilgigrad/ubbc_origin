                        <a href="https://utmb.world/utmb-index" target="_blank">
                            <img src="static/images/Index-50K.png" class="img-fluid" style="height: 1.5em;" alt="UTMB Index">
                        </a>
                    </div>
					<p class="fl-txt-lesslight">En achevant dix tours (ou plus) du parcours <strong>UBBC 10ème anniversaire</strong> (47 km et 2300 m de D+), vous devenez finisher de l’UBBC-K2. Autant être honnête: c’est quasiment impossible. Vous n’avez aucune chance. Vraiment aucune. On vous voit venir d’ici, vers minuit, en train de chouiner: <em> “gningningnin il ne me reste qu’un tour…”</em> mais il sera trop tard.
                        On ne pliera pas. Seuls les plus rapides, les plus endurants, les plus affûté·e·s et probablement les plus légèrement givré·e·s, pourront prétendre au titre.
                        Si vous ne montez pas régulièrement sur les podiums des plus grands trails du monde, on vous conseille gentiment de viser l’UBBC-Hipster. Ce sera déjà très bien.
                    </p>
