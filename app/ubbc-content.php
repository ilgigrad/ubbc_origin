	         <h3 class="fl-txt-rose mb-0 mr-2">UBBC-Golgoth</h3>
	        <p class="fl-txt-lesslight">L’UBBC-Golgoth est réservé à une poignée d’élites. Des coureurs capables de justifier d’un niveau de performance hors du commun, et surtout d’un certain goût pour l’absurde.
                Les finishers des éditions précédentes - 2019 incluse - brillent encore quelque part dans notre mémoire collective. On les regarde comme on regarde Ulysse, Achille, Hercule ou Andromède: avec admiration, incrédulité et un soupçon d’envie.
                Soyons clairs : il faut être sérieusement solide pour aller au bout de cette épreuve. Entraînés, affûtés, hermétiques au sommeil et aux distractions du base-camp. Ceux-là ont enchaîné les tours toute la nuit, sans flancher, sans traîner, sans céder.
                Franchir la ligne d’arrivée de l’urban-ultra-trail le plus dur du monde est devenu leur titre de gloire. Et même si personne ne s’en souvient vraiment, eux, au moins, savent ce qu’ils ont accompli.

            </p>
